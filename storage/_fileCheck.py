def clean_name(name):
    """Converts Underscore_Name to Underscore Name for matching."""
    return name.replace('_', ' ')

def validate_assets():
    # Find all enum blocks in the string
    enum_blocks = re.findall(r'enum\s+(\w+)\s*\{([\s\S]*?)\}', RAW_ENUM_DATA)
    
    if not enum_blocks:
        print("❌ No enums found in the input string.")
        return

    for enum_name, enum_content in enum_blocks:
        # 1. Map Enum Name to Folder Name (E_Male_Skin -> Male Skin)
        # Removes the 'E_' prefix and replaces underscores with spaces
        folder_name = clean_name(re.sub(r'^E_', '', enum_name))
        target_path = os.path.join(INPUT_DIRECTORY, folder_name)

        # 2. Parse Enum Values (Exclude 'None')
        raw_values = [v.strip() for v in enum_content.split(',') if v.strip()]
        enum_traits = [v for v in raw_values if v.lower() != 'none']
        
        # Create a mapping of "Human 1" (from enum) to "Human_1" (original enum key)
        enum_mapped_names = {clean_name(v): v for v in enum_traits}

        print(f"\n  {enum_name} --> {folder_name}")

        if not os.path.exists(target_path):
            print(f"  ❌ FOLDER MISSING: Expected path '{target_path}'")
            print("-" * 30)
            continue

        # 3. Get PNG files from Directory (Exclude 'None.png' if it exists)
        files_in_dir = [f for f in os.listdir(target_path) if f.lower().endswith('.png')]
        # Clean file names: "Human 1.png" -> "Human 1"
        dir_traits = {clean_name(os.path.splitext(f)[0]): f for f in files_in_dir if f.lower() != 'none.png'}

        # 4. Compare Sets
        enum_set = set(enum_mapped_names.keys())
        dir_set = set(dir_traits.keys())

        missing_pngs = enum_set - dir_set
        extra_pngs = dir_set - enum_set

        # 5. Output Results
        all_match = True
        
        # Check counts
        if len(enum_set) == len(dir_set):
            print(f"\n{GREEN}  ✅ Items: {len(enum_set)} {RESET}")
        else:
            print(f"\n{RED}  ❌ COUNT MISMATCH: Enum has {len(enum_set)}, Folder has {len(dir_set)}.{RESET}")
            all_match = False

        # Traits in Enum but no PNG
        for m in missing_pngs:
            orig_name = enum_mapped_names[m]
            print(f"\n{RED}  ❌ MISSING PNG: No file for enum '{orig_name}' (Expected {m}.png){RESET}")
            all_match = False

        # PNGs in Folder but no Enum entry
        for e in extra_pngs:
            orig_file = dir_traits[e]
            print(f"\n{RED}  ❌ EXTRA PNG: File '{orig_file}' exists but is not in Enum.{RESET}")
            all_match = False

        if all_match:
            print(f"\n{GREEN}  ✅ Status: Successful{RESET}")
        
        print("\n\n")

if __name__ == "__main__":
    validate_assets()